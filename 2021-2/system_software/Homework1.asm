FIRST		LDA DIFF
CPYLOOP		JSUB READC
		JSUB TOUPPER
		JSUB WRITEC
		COMP ENDWORD
		JEQ ENDLOOP
		J CPYLOOP 
ENDLOOP		LDA DIFF	
		END FIRST
		.
		.
.
READC		TD INDEV
		JEQ READC 
		RD INDEV
		RSUB
		.
		.
.
WRITEC		TD OUTDEV
		JEQ WRITEC
		WD OUTDEV
		RSUB
		.
		.
.
TOUPPER		COMP LCSTART
		JLT ENDUPPER
		COMP LCEND
		JGT ENDUPPER
		SUB DIFF
ENDUPPER	RSUB


INDEV		BYTE X'F1'
OUTDEV		BYTE X'F2'
.
DIFF		WORD 32
ENDWORD		WORD X'000024'
LCSTART		WORD X'000061'
LCEND		WORD X'00007A'
