HM2	START 0
FIRST	LDX TOP
TITLE	TD OUTDEV
	JEQ TITLE
	LDA SPACE
	WD OUTDEV
CLOOP	RMO X, S
	JSUB PRINT
	TIX BOTTOM
	JLT CLOOP
	JSUB CRLF
	LDX TOP
ROW	TD OUTDEV
	JEQ ROW	
	RMO X, T
	RMO X, A
	ADD SHIFT
	WD OUTDEV
	LDX TOP
COL	RMO X, S
	MULR T, S
	JSUB PRINT
	TIX BOTTOM
	JLT COL
	JSUB CRLF
	RMO T, X
	TIX BOTTOM
	JLT ROW
FIN	J FIN
.
PRINT	TD OUTDEV
	JEQ PRINT
	LDA SPACE
	WD OUTDEV
	LDA #9
	COMPR S, A
	JGT TDIG
	LDA SPACE
	WD OUTDEV
ODIG	LDA SHIFT
	ADDR S, A
	WD OUTDEV
	J ENDP
TDIG	RMO S, A
	DIV #10
	ADD SHIFT
	WD OUTDEV
	SUB SHIFT
	MUL #10
	SUBR A, S
	J ODIG	
ENDP	RSUB
.
CRLF	TD OUTDEV
	JEQ CRLF
	LDA CR
	WD OUTDEV
	LDA LF
	WD OUTDEV
	RSUB
.	
TOP	WORD 1
BOTTOM	WORD 10
CR	WORD 0x0D
LF	WORD 0x0A
SPACE	WORD 0x20
SHIFT	WORD 0x30
.
OUTDEV	BYTE X'F2'
	END FIRST